package com.documentos;

/**
 *
 * @author Villalba
 */
public class documento {
    private int id;
    private int id_det;

    public documento() {
        this.id = 0;
        this.id_det = 0;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public int getId_det() {
        return id_det;
    }

    public void setId_det(int id_det) {
        this.id_det = id_det;
    }
    
}
